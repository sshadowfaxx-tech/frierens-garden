class TransactionParser:
    """
    Parser for Solana transactions.
    Extracts token swaps, transfers, and relevant metadata.
    """
    
    # Known DEX program IDs
    DEX_PROGRAMS = {
        Config.JUPITER_PROGRAM_ID: DEXType.JUPITER,
        Config.RAYDIUM_PROGRAM_ID: DEXType.RAYDIUM,
        Config.RAYDIUM_CONCENTRATED: DEXType.RAYDIUM_CLMM,
    }
    
    # Wrapped SOL mint
    WRAPPED_SOL = "So11111111111111111111111111111111111111112"
    
    def __init__(self, client: HeliusClient):
        self.client = client
        self.logger = logging.getLogger(__name__)
        
    def detect_dex(self, transaction: Dict) -> DEXType:
        """
        Detect which DEX was used in the transaction.
        
        Args:
            transaction: Transaction data from Helius
            
        Returns:
            DEX type
        """
        account_keys = self._get_account_keys(transaction)
        
        for program_id, dex_type in self.DEX_PROGRAMS.items():
            if program_id in account_keys:
                return dex_type
                
        # Check inner instructions for program calls
        meta = transaction.get("meta", {})
        inner_instructions = meta.get("innerInstructions", [])
        
        for inner in inner_instructions:
            for instruction in inner.get("instructions", []):
                program_id = instruction.get("programId", "")
                if program_id in self.DEX_PROGRAMS:
                    return self.DEX_PROGRAMS[program_id]
                    
        return DEXType.UNKNOWN
    
    def _get_account_keys(self, transaction: Dict) -> List[str]:
        """Extract account keys from transaction."""
        tx_data = transaction.get("transaction", {})
        message = tx_data.get("message", {})
        
        account_keys = []
        
        # Handle both legacy and versioned transactions
        if "accountKeys" in message:
            account_keys = [
                acc.get("pubkey") if isinstance(acc, dict) else acc
                for acc in message.get("accountKeys", [])
            ]
        elif "accounts" in message:
            account_keys = message.get("accounts", [])
            
        return account_keys
    
    def parse_token_transfers(self, transaction: Dict, wallet: str) -> List[TokenTransfer]:
        """
        Parse all token transfers from a transaction.
        
        Args:
            transaction: Transaction data
            wallet: Wallet address being analyzed
            
        Returns:
            List of token transfers
        """
        transfers = []
        meta = transaction.get("meta", {})
        
        # Get pre and post token balances
        pre_balances = {b["mint"]: b for b in meta.get("preTokenBalances", [])}
        post_balances = {b["mint"]: b for b in meta.get("postTokenBalances", [])}
        
        # Parse from token balance changes
        all_mints = set(pre_balances.keys()) | set(post_balances.keys())
        
        for mint in all_mints:
            pre = pre_balances.get(mint, {})
            post = post_balances.get(mint, {})
            
            pre_amount = float(pre.get("uiTokenAmount", {}).get("amount", 0))
            post_amount = float(post.get("uiTokenAmount", {}).get("amount", 0))
            decimals = pre.get("uiTokenAmount", {}).get("decimals") or post.get("uiTokenAmount", {}).get("decimals", 0)
            
            # Find owner in balance data
            pre_owner = pre.get("owner", "")
            post_owner = post.get("owner", "")
            
            amount_change = post_amount - pre_amount
            
            if amount_change != 0:
                transfers.append(TokenTransfer(
                    mint=mint,
                    amount=abs(amount_change),
                    decimals=decimals,
                    from_address=pre_owner if amount_change < 0 else "",
                    to_address=post_owner if amount_change > 0 else "",
                    is_native=(mint == self.WRAPPED_SOL)
                ))
        
        # Also check inner instructions for transfers
        inner_instructions = meta.get("innerInstructions", [])
        for inner in inner_instructions:
            for instruction in inner.get("instructions", []):
                parsed = instruction.get("parsed", {})
                if parsed.get("type") == "transfer":
                    info = parsed.get("info", {})
                    # Handle both token and SOL transfers
                    if "mint" in info:
                        transfers.append(TokenTransfer(
                            mint=info.get("mint"),
                            amount=float(info.get("amount", 0)),
                            decimals=int(info.get("decimals", 0)),
                            from_address=info.get("authority", info.get("source", "")),
                            to_address=info.get("destination", ""),
                            is_native=(info.get("mint") == self.WRAPPED_SOL)
                        ))
        
        return transfers
    
    def parse_swap(
        self,
        transaction: Dict,
        signature: str,
        wallet: str
    ) -> Optional[TokenSwap]:
        """
        Parse a token swap from a transaction.
        
        Args:
            transaction: Transaction data from Helius
            signature: Transaction signature
            wallet: Wallet address
            
        Returns:
            TokenSwap object or None if not a valid swap
        """
        try:
            # Detect DEX
            dex = self.detect_dex(transaction)
            if dex == DEXType.UNKNOWN:
                return None
            
            # Get transaction metadata
            block_time = transaction.get("blockTime", 0)
            slot = transaction.get("slot", 0)
            timestamp = datetime.fromtimestamp(block_time)
            
            # Parse token transfers
            transfers = self.parse_token_transfers(transaction, wallet)
            
            if len(transfers) < 2:
                return None
            
            # Determine which transfers involve the wallet
            wallet_transfers = [
                t for t in transfers
                if wallet in [t.from_address, t.to_address]
            ]
            
            if len(wallet_transfers) < 2:
                return None
            
            # Classify as buy or sell based on SOL movement
            # Buy: SOL out, token in
            # Sell: token out, SOL in
            sol_transfers = [t for t in wallet_transfers if t.is_native]
            token_transfers = [t for t in wallet_transfers if not t.is_native]
            
            if not sol_transfers or not token_transfers:
                # Try to determine from transfer direction
                outgoing = [t for t in wallet_transfers if t.from_address == wallet]
                incoming = [t for t in wallet_transfers if t.to_address == wallet]
                
                if len(outgoing) == 1 and len(incoming) == 1:
                    token_in = incoming[0]
                    token_out = outgoing[0]
                    
                    # Determine trade type
                    if token_out.is_native:
                        trade_type = TradeType.BUY
                    elif token_in.is_native:
                        trade_type = TradeType.SELL
                    else:
                        trade_type = TradeType.UNKNOWN
                        
                    return TokenSwap(
                        signature=signature,
                        timestamp=timestamp,
                        dex=dex,
                        token_in=token_in,
                        token_out=token_out,
                        trade_type=trade_type,
                        wallet=wallet,
                        slot=slot,
                        block_time=block_time
                    )
                return None
            
            # Standard SOL-based swap
            sol_transfer = sol_transfers[0]
            token_transfer = token_transfers[0]
            
            if sol_transfer.from_address == wallet:
                # SOL leaving wallet = BUY
                token_in = token_transfer
                token_out = sol_transfer
                trade_type = TradeType.BUY
            else:
                # SOL entering wallet = SELL
                token_in = sol_transfer
                token_out = token_transfer
                trade_type = TradeType.SELL
            
            return TokenSwap(
                signature=signature,
                timestamp=timestamp,
                dex=dex,
                token_in=token_in,
                token_out=token_out,
                trade_type=trade_type,
                wallet=wallet,
                slot=slot,
                block_time=block_time
            )
            
        except Exception as e:
            self.logger.debug(f"Failed to parse swap {signature}: {e}")
            return None
    
    def get_token_decimals(self, mint: str) -> int:
        """Get token decimals, using cache when available."""
        if mint in Config.DECIMALS_CACHE:
            return Config.DECIMALS_CACHE[mint]
        
        # Default to 9 for SOL, 6 for most tokens
        default = 9 if mint == self.WRAPPED_SOL else 6
        
        try:
            account_info = self.client.get_account_info(mint)
            if account_info:
                data = account_info.get("data", [])
                if data and len(data) > 0:
                    parsed = data[0] if isinstance(data, list) else data
                    if isinstance(parsed, dict):
                        decimals = parsed.get("parsed", {}).get("info", {}).get("decimals")
                        if decimals is not None:
                            Config.DECIMALS_CACHE[mint] = decimals
                            return decimals
        except Exception as e:
            self.logger.debug(f"Failed to get decimals for {mint}: {e}")
        
        Config.DECIMALS_CACHE[mint] = default
        return default


