class HeliusClient:
    """
    Client for interacting with the Helius API.
    Handles rate limiting, retries, and pagination.
    """
    
    def __init__(self, api_key: str):
        self.api_key = api_key
        self.base_url = Config.HELIUS_BASE_URL
        self.session = requests.Session()
        self.logger = logging.getLogger(__name__)
        
    def _make_request(self, method: str, params: Dict = None) -> Dict:
        """
        Make a request to the Helius API with retry logic.
        
        Args:
            method: RPC method name
            params: Request parameters
            
        Returns:
            JSON response data
            
        Raises:
            requests.RequestException: If all retries fail
        """
        url = f"{self.base_url}/?api-key={self.api_key}"
        payload = {
            "jsonrpc": "2.0",
            "id": 1,
            "method": method,
            "params": params or []
        }
        
        for attempt in range(Config.MAX_RETRIES):
            try:
                response = self.session.post(
                    url,
                    json=payload,
                    timeout=Config.REQUEST_TIMEOUT
                )
                response.raise_for_status()
                data = response.json()
                
                if "error" in data:
                    raise Exception(f"API Error: {data['error']}")
                    
                return data.get("result", {})
                
            except requests.RequestException as e:
                self.logger.warning(f"Request failed (attempt {attempt + 1}): {e}")
                if attempt < Config.MAX_RETRIES - 1:
                    time.sleep(Config.RETRY_DELAY * (attempt + 1))
                else:
                    raise
                    
        return {}
    
    def get_signatures_for_address(
        self,
        address: str,
        limit: int = 100,
        before: Optional[str] = None,
        until: Optional[str] = None
    ) -> List[Dict]:
        """
        Fetch transaction signatures for an address.
        
        Args:
            address: Wallet or program address
            limit: Maximum signatures to fetch (max 1000)
            before: Fetch signatures before this signature
            until: Fetch signatures until this signature
            
        Returns:
            List of signature data
        """
        params = {
            "limit": min(limit, 1000),
            "commitment": "confirmed"
        }
        if before:
            params["before"] = before
        if until:
            params["until"] = until
            
        result = self._make_request(
            "getSignaturesForAddress",
            [address, params]
        )
        return result if isinstance(result, list) else []
    
    def get_transaction(self, signature: str, max_supported_transaction_version: int = 0) -> Optional[Dict]:
        """
        Fetch detailed transaction data.
        
        Args:
            signature: Transaction signature
            max_supported_transaction_version: Maximum transaction version to support
            
        Returns:
            Transaction data or None if not found
        """
        params = {
            "encoding": "jsonParsed",
            "maxSupportedTransactionVersion": max_supported_transaction_version,
            "commitment": "confirmed"
        }
        
        result = self._make_request("getTransaction", [signature, params])
        return result
    
    def get_account_info(self, address: str) -> Optional[Dict]:
        """
        Fetch account information.
        
        Args:
            address: Account address
            
        Returns:
            Account data or None if not found
        """
        params = {
            "encoding": "jsonParsed",
            "commitment": "confirmed"
        }
        
        result = self._make_request("getAccountInfo", [address, params])
        return result.get("value") if result else None
    
    def get_token_metadata(self, mint: str) -> Dict:
        """
        Fetch token metadata using Helius enhanced API.
        
        Args:
            mint: Token mint address
            
        Returns:
            Token metadata
        """
        # Try to get from enhanced API
        url = f"https://mainnet.helius-rpc.com/?api-key={self.api_key}"
        payload = {
            "jsonrpc": "2.0",
            "id": 1,
            "method": "getAsset",
            "params": {"id": mint}
        }
        
        try:
            response = self.session.post(url, json=payload, timeout=Config.REQUEST_TIMEOUT)
            data = response.json()
            return data.get("result", {})
        except Exception as e:
            self.logger.warning(f"Failed to fetch metadata for {mint}: {e}")
            return {}
    
    def get_all_transactions(
        self,
        address: str,
        limit: Optional[int] = None,
        days_back: Optional[int] = None
    ) -> List[Dict]:
        """
        Fetch all transactions for an address with pagination.
        
        Args:
            address: Wallet address
            limit: Maximum transactions to fetch (None for all)
            days_back: Only fetch transactions from last N days
            
        Returns:
            List of transaction signatures
        """
        all_signatures = []
        before = None
        batch_size = 100
        
        # Calculate cutoff timestamp if days_back specified
        cutoff_time = None
        if days_back:
            cutoff_time = int((datetime.now() - timedelta(days=days_back)).timestamp())
        
        while True:
            # Check if we've reached the limit
            if limit and len(all_signatures) >= limit:
                break
                
            current_batch_size = min(batch_size, limit - len(all_signatures)) if limit else batch_size
            
            signatures = self.get_signatures_for_address(
                address,
                limit=current_batch_size,
                before=before
            )
            
            if not signatures:
                break
                
            # Check time cutoff
            if cutoff_time:
                valid_signatures = [
                    sig for sig in signatures 
                    if sig.get("blockTime", 0) > cutoff_time
                ]
                all_signatures.extend(valid_signatures)
                
                # If we filtered some out, we've reached the cutoff
                if len(valid_signatures) < len(signatures):
                    break
            else:
                all_signatures.extend(signatures)
            
            # Set before for next iteration
            before = signatures[-1].get("signature")
            
            # Stop if we got fewer than requested
            if len(signatures) < current_batch_size:
                break
                
        return all_signatures[:limit] if limit else all_signatures


