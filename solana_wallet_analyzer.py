@dataclass
class TokenTransfer:
    """Represents a token transfer in a transaction."""
    mint: str
    amount: float
    decimals: int
    from_address: str
    to_address: str
    is_native: bool = False
    
    @property
    def normalized_amount(self) -> float:
        """Return amount normalized by decimals."""
        return self.amount / (10 ** self.decimals)


@dataclass
class TokenSwap:
    """Represents a token swap transaction."""
    signature: str
    timestamp: datetime
    dex: DEXType
    token_in: TokenTransfer
    token_out: TokenTransfer
    trade_type: TradeType
    wallet: str
    slot: int
    block_time: int
    
    # Calculated fields
    price: float = 0.0
    value_usd: float = 0.0
    
    def __post_init__(self):
        """Calculate price after initialization."""
        if self.token_out.normalized_amount > 0:
            self.price = self.token_in.normalized_amount / self.token_out.normalized_amount


@dataclass
class Position:
    """Tracks a trading position for a specific token."""
    token_mint: str
    token_symbol: str = ""
    entry_price: float = 0.0
    total_bought: float = 0.0
    total_spent: float = 0.0
    total_sold: float = 0.0
    total_received: float = 0.0
    remaining_quantity: float = 0.0
    first_buy_slot: Optional[int] = None
    last_buy_time: Optional[datetime] = None
    
    @property
    def avg_buy_price(self) -> float:
        """Calculate average buy price."""
        if self.total_bought > 0:
            return self.total_spent / self.total_bought
        return 0.0
    
    @property
    def realized_pnl(self) -> float:
        """Calculate realized PnL (from completed trades)."""
        return self.total_received - (self.total_sold * self.avg_buy_price)
    
    @property
    def unrealized_pnl(self, current_price: float = 0) -> float:
        """Calculate unrealized PnL (from remaining position)."""
        if current_price > 0:
            return self.remaining_quantity * (current_price - self.avg_buy_price)
        return 0.0


