def setup_logging(verbose: bool = False):
    """Configure logging."""
    level = logging.DEBUG if verbose else logging.INFO
    logging.basicConfig(
        level=level,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
        datefmt='%Y-%m-%d %H:%M:%S'
    )


def load_wallets_from_file(filepath: str) -> List[str]:
    """Load wallet addresses from a file (one per line)."""
    wallets = []
    path = Path(filepath)
    
    if not path.exists():
        raise FileNotFoundError(f"Wallet file not found: {filepath}")
    
    with open(path, 'r') as f:
        for line in f:
            wallet = line.strip()
            if wallet and not wallet.startswith('#'):
                wallets.append(wallet)
    
    return wallets


def validate_wallet_address(address: str) -> bool:
    """Basic validation for Solana wallet addresses."""
    # Solana addresses are base58-encoded and 32-44 characters
    if not address or len(address) < 32 or len(address) > 44:
        return False
    # Basic base58 check (alphanumeric excluding 0, O, I, l)
    valid_chars = set('123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz')
    return all(c in valid_chars for c in address)


def main():
    """Main entry point for the analyzer."""
    parser = argparse.ArgumentParser(
        description="Analyze Solana wallet profitability using Helius API",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Analyze single wallet
  python solana_wallet_analyzer.py --wallets 7xKXtg2CW87d97TXJSDpbD5jBkheTqA83TZRuJosgAsU --api-key YOUR_API_KEY
  
  # Analyze multiple wallets
  python solana_wallet_analyzer.py --wallets WALLET1 WALLET2 WALLET3 --api-key YOUR_API_KEY
  
  # Load wallets from file
  python solana_wallet_analyzer.py --wallet-file wallets.txt --api-key YOUR_API_KEY
  
  # Limit to recent activity and output as JSON
  python solana_wallet_analyzer.py --wallets WALLET1 --days-back 30 --output json --api-key YOUR_API_KEY
        """
    )
    
    # Input options
    input_group = parser.add_mutually_exclusive_group(required=True)
    input_group.add_argument(
        '--wallets',
        nargs='+',
        help='One or more wallet addresses to analyze'
    )
    input_group.add_argument(
        '--wallet-file',
        help='File containing wallet addresses (one per line)'
    )
    
    # API configuration
    parser.add_argument(
        '--api-key',
        default=os.getenv('HELIUS_API_KEY'),
        help='Helius API key (or set HELIUS_API_KEY env var)'
    )
    
    # Analysis options
    parser.add_argument(
        '--days-back',
        type=int,
        help='Only analyze transactions from last N days'
    )
    parser.add_argument(
        '--tx-limit',
        type=int,
        help='Maximum transactions to analyze per wallet'
    )
    parser.add_argument(
        '--parallel',
        type=int,
        default=3,
        help='Number of parallel workers (default: 3)'
    )
    
    # Output options
    parser.add_argument(
        '--output',
        choices=['console', 'json', 'csv'],
        default='console',
        help='Output format (default: console)'
    )
    parser.add_argument(
        '--output-file',
        help='Save output to file instead of stdout'
    )
    parser.add_argument(
        '--verbose',
        '-v',
        action='store_true',
        help='Enable verbose logging and output'
    )
    
    args = parser.parse_args()
    
    # Setup logging
    setup_logging(args.verbose)
    logger = logging.getLogger(__name__)
    
    # Validate API key
    if not args.api_key:
        logger.error("Helius API key is required. Use --api-key or set HELIUS_API_KEY env var.")
        sys.exit(1)
    
    # Load wallets
    try:
        if args.wallets:
            wallets = args.wallets
        else:
            wallets = load_wallets_from_file(args.wallet_file)
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    
    # Validate wallet addresses
    valid_wallets = []
    for wallet in wallets:
        if validate_wallet_address(wallet):
            valid_wallets.append(wallet)
        else:
            logger.warning(f"Invalid wallet address skipped: {wallet}")
    
    if not valid_wallets:
        logger.error("No valid wallet addresses provided.")
        sys.exit(1)
    
    logger.info(f"Analyzing {len(valid_wallets)} wallet(s)...")
    
    # Initialize components
    client = HeliusClient(args.api_key)
    tx_parser = TransactionParser(client)
    analyzer = ProfitabilityAnalyzer(client, tx_parser)
    
    # Analyze wallets
    analyses = []
    
    def analyze_single(wallet: str) -> WalletAnalysis:
        try:
            return analyzer.analyze_wallet(
                wallet,
                days_back=args.days_back,
                tx_limit=args.tx_limit
            )
        except Exception as e:
            logger.error(f"Failed to analyze {wallet}: {e}")
            return WalletAnalysis(wallet_address=wallet)
    
    # Use parallel processing for multiple wallets
    if len(valid_wallets) > 1 and args.parallel > 1:
        with concurrent.futures.ThreadPoolExecutor(max_workers=args.parallel) as executor:
            analyses = list(executor.map(analyze_single, valid_wallets))
    else:
        for wallet in valid_wallets:
            analyses.append(analyze_single(wallet))
    
    # Rank wallets
    ranked_analyses = analyzer.rank_wallets(analyses)
    
    # Format output
    formatter = OutputFormatter()
    
    if args.output == 'json':
        output = formatter.format_json(ranked_analyses)
    elif args.output == 'csv':
        output = formatter.format_csv(ranked_analyses)
    else:
        output = formatter.format_console(ranked_analyses, verbose=args.verbose)
    
    # Write or print output
    if args.output_file:
        with open(args.output_file, 'w') as f:
            f.write(output)
        logger.info(f"Results saved to {args.output_file}")
    else:
        print(output)
    
    # Summary statistics
    profitable_wallets = sum(1 for a in analyses if a.total_pnl > 0)
    repeat_winners = sum(1 for a in analyses if a.is_repeat_winner)
    
    logger.info("=" * 50)
    logger.info(f"Analysis complete!")
    logger.info(f"  Wallets analyzed: {len(analyses)}")
    logger.info(f"  Profitable wallets: {profitable_wallets}")
    logger.info(f"  Repeat winners: {repeat_winners}")
    logger.info("=" * 50)


if __name__ == "__main__":
    main()
