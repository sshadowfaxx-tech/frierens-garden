class ProfitabilityAnalyzer:
    """
    Analyzes wallet trading profitability.
    Calculates PnL, ROI, and identifies patterns.
    """
    
    def __init__(self, client: HeliusClient, parser: TransactionParser):
        self.client = client
        self.parser = parser
        self.logger = logging.getLogger(__name__)
        
    def analyze_wallet(
        self,
        wallet: str,
        days_back: Optional[int] = None,
        tx_limit: Optional[int] = None
    ) -> WalletAnalysis:
        """
        Perform complete profitability analysis on a wallet.
        
        Args:
            wallet: Wallet address
            days_back: Only analyze transactions from last N days
            tx_limit: Maximum transactions to analyze
            
        Returns:
            WalletAnalysis object with complete analysis
        """
        analysis = WalletAnalysis(wallet_address=wallet)
        
        # Fetch all transaction signatures
        self.logger.info(f"Fetching transactions for {wallet}...")
        signatures = self.client.get_all_transactions(wallet, tx_limit, days_back)
        
        if not signatures:
            self.logger.warning(f"No transactions found for {wallet}")
            return analysis
        
        self.logger.info(f"Found {len(signatures)} transactions, analyzing...")
        
        # Process each transaction
        for sig_data in signatures:
            signature = sig_data.get("signature")
            block_time = sig_data.get("blockTime", 0)
            
            if not signature:
                continue
            
            # Update time range
            tx_time = datetime.fromtimestamp(block_time)
            if analysis.first_trade_time is None or tx_time < analysis.first_trade_time:
                analysis.first_trade_time = tx_time
            if analysis.last_trade_time is None or tx_time > analysis.last_trade_time:
                analysis.last_trade_time = tx_time
            
            # Fetch full transaction
            try:
                tx = self.client.get_transaction(signature)
                if not tx:
                    continue
                    
                # Parse swap
                swap = self.parser.parse_swap(tx, signature, wallet)
                if swap:
                    analysis.swaps.append(swap)
                    self._process_swap(analysis, swap)
                    
            except Exception as e:
                self.logger.debug(f"Failed to process transaction {signature}: {e}")
                continue
        
        # Calculate final statistics
        analysis.calculate_summary_stats()
        
        # Analyze patterns
        self._analyze_early_bird_patterns(analysis)
        self._analyze_consistency(analysis)
        
        return analysis
    
    def _process_swap(self, analysis: WalletAnalysis, swap: TokenSwap):
        """Process a swap and update analysis."""
        analysis.total_trades += 1
        
        # Get or create position for the token
        token_mint = swap.token_out.mint if swap.trade_type == TradeType.BUY else swap.token_in.mint
        
        if token_mint not in analysis.positions:
            analysis.positions[token_mint] = Position(token_mint=token_mint)
        
        position = analysis.positions[token_mint]
        
        if swap.trade_type == TradeType.BUY:
            # Buying token
            amount_bought = swap.token_out.normalized_amount
            cost = swap.token_in.normalized_amount
            
            position.total_bought += amount_bought
            position.total_spent += cost
            position.remaining_quantity += amount_bought
            
            if position.first_buy_slot is None:
                position.first_buy_slot = swap.slot
            position.last_buy_time = swap.timestamp
            
        elif swap.trade_type == TradeType.SELL:
            # Selling token
            amount_sold = swap.token_out.normalized_amount
            proceeds = swap.token_in.normalized_amount
            
            position.total_sold += amount_sold
            position.total_received += proceeds
            position.remaining_quantity = max(0, position.remaining_quantity - amount_sold)
            
            # Determine if profitable
            if position.avg_buy_price > 0:
                sell_price = proceeds / amount_sold if amount_sold > 0 else 0
                profit_ratio = (sell_price - position.avg_buy_price) / position.avg_buy_price
                
                if profit_ratio > Config.PROFITABLE_THRESHOLD:
                    analysis.profitable_trades += 1
                elif profit_ratio < -Config.PROFITABLE_THRESHOLD:
                    analysis.losing_trades += 1
    
    def _analyze_early_bird_patterns(self, analysis: WalletAnalysis):
        """
        Analyze early bird patterns (buying tokens early).
        
        A wallet is considered an "early bird" if it buys tokens
        within the first N blocks of their existence.
        """
        early_bird_wins = 0
        early_bird_total = 0
        
        for swap in analysis.swaps:
            if swap.trade_type != TradeType.BUY:
                continue
            
            # Check if this is an early purchase
            # We need to compare against token creation slot
            # For now, we'll use a heuristic based on position first buy
            position = analysis.positions.get(swap.token_out.mint)
            if position and position.first_buy_slot:
                blocks_since_creation = swap.slot - position.first_buy_slot
                
                if blocks_since_creation <= Config.EARLY_BIRD_BLOCKS:
                    early_bird_total += 1
                    
                    # Check if this early buy was profitable
                    if position.realized_pnl > 0:
                        early_bird_wins += 1
        
        analysis.early_bird_count = early_bird_total
        if early_bird_total > 0:
            analysis.early_bird_success_rate = early_bird_wins / early_bird_total
    
    def _analyze_consistency(self, analysis: WalletAnalysis):
        """
        Analyze trading consistency and identify repeat winners.
        
        Consistent traders have:
        - High win rate (>60%)
        - Multiple profitable trades
        - Positive overall PnL
        """
        if analysis.total_trades < Config.MIN_TRADES_FOR_PATTERN:
            return
        
        # Calculate consistency score
        win_rate_score = analysis.win_rate
        volume_score = min(analysis.total_trades / 50, 1.0)  # Normalize to 50 trades
        pnl_score = min(max(analysis.total_roi / 1.0, -1.0), 1.0)  # Normalize to 100% ROI
        
        analysis.consistent_profit_score = (
            win_rate_score * 0.4 +
            volume_score * 0.2 +
            max(pnl_score, 0) * 0.4
        )
        
        # Mark as repeat winner if criteria met
        if (analysis.win_rate >= 0.6 and 
            analysis.profitable_trades >= Config.MIN_TRADES_FOR_PATTERN and
            analysis.total_pnl > 0):
            analysis.is_repeat_winner = True
    
    def rank_wallets(self, analyses: List[WalletAnalysis]) -> List[WalletAnalysis]:
        """
        Rank wallets by profitability.
        
        Ranking criteria (weighted):
        - Total PnL (30%)
        - ROI (25%)
        - Win rate (20%)
        - Consistency score (15%)
        - Early bird success (10%)
        
        Args:
            analyses: List of wallet analyses
            
        Returns:
            Sorted list (highest ranked first)
        """
        def calculate_score(a: WalletAnalysis) -> float:
            # Normalize metrics to 0-1 scale
            max_pnl = max((x.total_pnl for x in analyses), default=1) or 1
            max_roi = max((x.total_roi for x in analyses), default=1) or 1
            
            pnl_score = max(a.total_pnl, 0) / max_pnl if max_pnl > 0 else 0
            roi_score = max(a.total_roi, 0) / max_roi if max_roi > 0 else 0
            
            return (
                pnl_score * 0.30 +
                roi_score * 0.25 +
                a.win_rate * 0.20 +
                a.consistent_profit_score * 0.15 +
                a.early_bird_success_rate * 0.10
            )
        
        return sorted(analyses, key=calculate_score, reverse=True)


