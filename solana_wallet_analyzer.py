class OutputFormatter:
    """Formats and outputs analysis results."""
    
    @staticmethod
    def format_console(analyses: List[WalletAnalysis], verbose: bool = False) -> str:
        """Format results for console output."""
        lines = []
        lines.append("=" * 100)
        lines.append("SOLANA WALLET PROFITABILITY ANALYSIS")
        lines.append("=" * 100)
        lines.append("")
        
        for i, analysis in enumerate(analyses, 1):
            lines.append(f"#{i} Wallet: {analysis.wallet_address}")
            lines.append("-" * 100)
            lines.append(f"  Total Trades:      {analysis.total_trades}")
            lines.append(f"  Profitable Trades: {analysis.profitable_trades}")
            lines.append(f"  Losing Trades:     {analysis.losing_trades}")
            lines.append(f"  Win Rate:          {analysis.win_rate:.2%}")
            lines.append(f"  Total PnL:         {analysis.total_pnl:.6f} SOL")
            lines.append(f"  Total ROI:         {analysis.total_roi:.2%}")
            lines.append(f"  Avg Trade Size:    {analysis.avg_trade_size:.6f} SOL")
            lines.append("")
            lines.append(f"  Early Bird Trades: {analysis.early_bird_count}")
            lines.append(f"  Early Bird Success: {analysis.early_bird_success_rate:.2%}")
            lines.append(f"  Repeat Winner:     {'Yes' if analysis.is_repeat_winner else 'No'}")
            lines.append(f"  Consistency Score: {analysis.consistent_profit_score:.4f}")
            
            if analysis.first_trade_time:
                lines.append(f"  First Trade:       {analysis.first_trade_time.strftime('%Y-%m-%d %H:%M')}")
            if analysis.last_trade_time:
                lines.append(f"  Last Trade:        {analysis.last_trade_time.strftime('%Y-%m-%d %H:%M')}")
            
            if verbose and analysis.positions:
                lines.append("")
                lines.append("  Top Positions:")
                sorted_positions = sorted(
                    analysis.positions.values(),
                    key=lambda p: abs(p.realized_pnl),
                    reverse=True
                )[:5]
                
                for pos in sorted_positions:
                    lines.append(f"    {pos.token_mint[:20]}...")
                    lines.append(f"      Bought: {pos.total_bought:.4f} | Sold: {pos.total_sold:.4f}")
                    lines.append(f"      PnL: {pos.realized_pnl:.6f} SOL")
                    lines.append(f"      Avg Buy: {pos.avg_buy_price:.8f}")
            
            lines.append("")
        
        return "\n".join(lines)
    
    @staticmethod
    def format_json(analyses: List[WalletAnalysis]) -> str:
        """Format results as JSON."""
        data = {
            "analysis_timestamp": datetime.now().isoformat(),
            "total_wallets_analyzed": len(analyses),
            "rankings": [a.to_dict() for a in analyses]
        }
        return json.dumps(data, indent=2)
    
    @staticmethod
    def format_csv(analyses: List[WalletAnalysis]) -> str:
        """Format results as CSV."""
        lines = [
            "rank,wallet_address,total_trades,profitable_trades,losing_trades,win_rate,total_pnl,total_roi,avg_trade_size,early_bird_count,early_bird_success_rate,is_repeat_winner,consistency_score"
        ]
        
        for i, a in enumerate(analyses, 1):
            lines.append(
                f"{i},{a.wallet_address},{a.total_trades},{a.profitable_trades},"
                f"{a.losing_trades},{a.win_rate:.4f},{a.total_pnl:.6f},"
                f"{a.total_roi:.4f},{a.avg_trade_size:.6f},{a.early_bird_count},"
                f"{a.early_bird_success_rate:.4f},{1 if a.is_repeat_winner else 0},"
                f"{a.consistent_profit_score:.4f}"
            )
        
        return "\n".join(lines)


